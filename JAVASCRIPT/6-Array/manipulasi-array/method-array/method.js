//method pada array

// 1. join
var arr = ['Mohammad', 'Iswandee', 'Ryu'];
console.log(arr.join(' '));

// 2. push & pop
arr.push('populer', 'terkenal', 'hebat')
console.log(arr.join(' '));

arr.pop()
console.log(arr.join(' '));

// 3. unshift & shift  
arr.unshift('Rank', 'S');
console.log(arr.join(' '));

arr.shift()
console.log(arr.join(' '));